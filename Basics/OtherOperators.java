public class OtherOperators
{
    public static void main(String[] args)
    {
        int x = 10;
        int y = 2;

      /* Agumented Assignment Operators
        x += y; (x=x+y) (result stored in x)
        x -= y; (x=x-y) (result stored in x)
        x *= y; (x=x*y) (result stored in x)
        x /= y; (x=x/y) (result stored in x)
        x %= y; (x=x%y) (result stored in x)*/

      /* Increment and Decrement Operators (used in looping)
        used when we just want to add or subtract 1.

        x++; (x=x+1) (result stored in x)
        y--; (y=y-1) (result stored in y)
       */
        
        System.out.println("x is "+x);
        System.out.println("y is "+y);

    }
}
