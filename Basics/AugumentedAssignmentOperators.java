public class AugumentedAssignmentOperators 
{
    public static void main(String[] args) {
      int var1 = 10;
      byte var2 = 3;
      var1 += var2;
      System.out.println(var1);
      var1 -= var2;
      System.out.println(var1);
      var1 *= var2;
      System.out.println(var1);
      var1 /= var2;
      System.out.println(var1);
   }
}
